# ── Step 6 — Application Admin Account ────────────────────────────────────────
class AdminAccountForm(FlaskForm):
    admin_username = StringField(
        "Admin Username",
        validators=[
            DataRequired(),
            Length(min=3, max=64),
            Regexp(r'^[a-zA-Z0-9_.-]+$',
                   message="Only letters, numbers, dots, hyphens and underscores.")
        ],
        render_kw={"placeholder": "orbitadmin"}
    )
    admin_email = StringField(
        "Admin Email Address",
        validators=[DataRequired(), Email(), Length(max=255)],
        render_kw={"placeholder": "admin@company.com"}
    )
    admin_first_name = StringField("First Name", validators=[DataRequired(), Length(max=64)])
    admin_last_name  = StringField("Last Name",  validators=[DataRequired(), Length(max=64)])
    admin_password = PasswordField(
        "Password",
        validators=[
            DataRequired(),
            Length(min=12, max=128,
                   message="Minimum 12 characters."),
            Regexp(r'^(?=.*[a-z])(?=.*[A-Z])(?=.*\d)(?=.*[@$!%*#?&^_\-])',
                   message="Must contain uppercase, lowercase, digit and special character.")
        ]
    )
    admin_password_confirm = PasswordField(
        "Confirm Password",
        validators=[
            DataRequired(),
            EqualTo("admin_password", message="Passwords must match.")
        ]
    )


