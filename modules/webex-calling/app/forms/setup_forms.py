from flask_wtf import FlaskForm
from flask_wtf.file import FileField, FileAllowed
from wtforms import (
    StringField, PasswordField, SelectField, BooleanField,
    IntegerField, TextAreaField, HiddenField, RadioField
)
from wtforms.validators import (
    DataRequired, Optional, Email, Length,
    NumberRange, EqualTo, URL, Regexp
)


# ── Step 1 — Branding ─────────────────────────────────────────────────────────
class BrandingForm(FlaskForm):
    app_name = StringField(
        "Application Name",
        validators=[DataRequired(), Length(min=2, max=64)],
        default="Orbit",
        render_kw={"placeholder": "Orbit"}
    )
    logo = FileField(
        "Logo File",
        validators=[
            Optional(),
            FileAllowed(["png", "svg", "jpg", "jpeg"],
                        "Only PNG, SVG, JPG files are accepted.")
        ]
    )
    primary_color = StringField(
        "Primary Brand Colour (hex)",
        validators=[
            DataRequired(),
            Regexp(r'^#[0-9A-Fa-f]{6}$', message="Must be a valid hex colour e.g. #1E40AF")
        ],
        default="#1E40AF"
    )
    accent_color = StringField(
        "Accent Colour (hex)",
        validators=[
            DataRequired(),
            Regexp(r'^#[0-9A-Fa-f]{6}$', message="Must be a valid hex colour.")
        ],
        default="#3B82F6"
    )
    company_name = StringField(
        "Company / Organisation Name",
        validators=[DataRequired(), Length(min=2, max=128)],
        render_kw={"placeholder": "ACME Corporation"}
    )
    timezone = SelectField(
        "Default Timezone",
        validators=[DataRequired()],
        default="UTC"
    )
    language = SelectField(
        "Default Language",
        choices=[
            ("en", "English"),
            ("nl", "Nederlands"),
            ("fr", "Français"),
            ("de", "Deutsch"),
            ("es", "Español"),
        ],
        default="en"
    )


# ── Step 2 — Network / FQDN / TLS ────────────────────────────────────────────
class NetworkForm(FlaskForm):
    fqdn = StringField(
        "Application FQDN",
        validators=[DataRequired(), Length(min=3, max=255)],
        render_kw={"placeholder": "orbit.company.com"}
    )
    cert_mode = RadioField(
        "TLS Certificate Mode",
        choices=[
            ("letsencrypt", "Let's Encrypt (automatic, requires public DNS)"),
            ("custom_ca",   "Custom CA / Bring Your Own Certificate"),
            ("selfsigned",  "Self-Signed (already generated by installer)"),
        ],
        default="selfsigned",
        validators=[DataRequired()]
    )
    # Let's Encrypt fields
    le_email = StringField(
        "Email for Let's Encrypt Notifications",
        validators=[Optional(), Email()],
        render_kw={"placeholder": "admin@company.com"}
    )
    le_agree_tos = BooleanField(
        "I agree to the Let's Encrypt Terms of Service",
        validators=[Optional()]
    )
    # Custom CA fields
    ca_cert_file   = FileField("CA Certificate (PEM/CRT)",
                               validators=[Optional(), FileAllowed(["pem", "crt", "cer"])])
    server_cert_file = FileField("Server Certificate (PEM/CRT)",
                                 validators=[Optional(), FileAllowed(["pem", "crt", "cer"])])
    server_key_file  = FileField("Server Private Key (PEM/KEY)",
                                 validators=[Optional(), FileAllowed(["pem", "key"])])


# ── Step 3 — LDAP ─────────────────────────────────────────────────────────────
