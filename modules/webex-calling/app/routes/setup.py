setup_bp = Blueprint("setup", __name__, template_folder="../templates/setup")

TOTAL_STEPS = 7
STEP_TITLES = {
    1: "Branding",
    2: "Network & TLS",
    3: "LDAP Directory",
    4: "Single Sign-On",
    5: "Email (SMTP)",
    6: "Admin Account",
    7: "Webex Integration",
}


def setup_only(f):
    """Decorator: block setup routes if setup is already complete."""
    @wraps(f)
    def decorated(*args, **kwargs):
        if os.environ.get("APP_STATE") == "setup_complete":
            flash("Setup has already been completed.", "info")
            return redirect(url_for("admin.dashboard"))
        return f(*args, **kwargs)
    return decorated


def _step_context(step: int) -> dict:
    """Build shared context dict for all setup templates."""
    completed = session.get("setup_completed_steps", [])
    return {
        "current_step":   step,
        "total_steps":    TOTAL_STEPS,
        "step_titles":    STEP_TITLES,
        "completed_steps": completed,
        "progress_pct":   int((len(completed) / TOTAL_STEPS) * 100),
    }


# ── Index — redirect to current step ─────────────────────────────────────────
@setup_bp.route("/", methods=["GET"])
@setup_only
def index():
    completed = session.get("setup_completed_steps", [])
    next_step = len(completed) + 1
    if next_step > TOTAL_STEPS:
        return redirect(url_for("setup.complete"))
    return redirect(url_for(f"setup.step{next_step}"))


# ── Step 1 — Branding ─────────────────────────────────────────────────────────
@setup_bp.route("/step/1", methods=["GET", "POST"])
@setup_only
def step1():
    form = BrandingForm()
    _populate_timezone_choices(form)

    if form.validate_on_submit():
        logo_path = ""
        if form.logo.data and form.logo.data.filename:
            ok, result = svc.save_logo(form.logo.data)
            if not ok:
                flash(result, "danger")
                return render_template(
                    "step1_branding.html", form=form, **_step_context(1)
                )
            logo_path = result
            flash(f"Logo uploaded: {logo_path}", "success")

        svc.save_branding(form.data, logo_path)
        _mark_step_complete(1)
        flash("Branding settings saved.", "success")
        return redirect(url_for("setup.step2"))

    return render_template("step1_branding.html", form=form, **_step_context(1))


# ── Step 2 — Network / TLS ────────────────────────────────────────────────────
@setup_bp.route("/step/2", methods=["GET", "POST"])
@setup_only
def step2():
    form = NetworkForm()

    if form.validate_on_submit():
        cert_mode = form.cert_mode.data
        fqdn      = form.fqdn.data.strip()

        if cert_mode == "letsencrypt":
            if not form.le_email.data:
                flash("An email address is required for Let's Encrypt.", "danger")
                return render_template("step2_network.html", form=form, **_step_context(2))
            if not form.le_agree_tos.data:
                flash("You must agree to the Let's Encrypt Terms of Service.", "danger")
                return render_template("step2_network.html", form=form, **_step_context(2))

            flash("Requesting Let's Encrypt certificate... this may take up to 60 seconds.", "info")
            ok, msg = svc.request_letsencrypt(fqdn, form.le_email.data)
            if not ok:
                flash(msg, "danger")
                return render_template("step2_network.html", form=form, **_step_context(2))
            flash(msg, "success")

        elif cert_mode == "custom_ca":
            if not (form.ca_cert_file.data and form.server_cert_file.data and form.server_key_file.data):
                flash("All three certificate files (CA cert, server cert, private key) are required.", "danger")
                return render_template("step2_network.html", form=form, **_step_context(2))

            ok, msg = svc.save_custom_cert(
                form.ca_cert_file.data,
                form.server_cert_file.data,
                form.server_key_file.data,
            )
            if not ok:
                flash(msg, "danger")
                return render_template("step2_network.html", form=form, **_step_context(2))
            flash(msg, "success")

        else:  # selfsigned — already generated by installer
            flash("Using self-signed certificate generated during installation.", "info")

        svc.save_network(fqdn, cert_mode)
        _mark_step_complete(2)
        flash("Network settings saved.", "success")
        return redirect(url_for("setup.step3"))

    return render_template("step2_network.html", form=form, **_step_context(2))


