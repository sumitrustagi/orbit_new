# ── Finalize — write HTTPS Nginx config, mark complete ────────────────────────
@setup_bp.route("/finalize", methods=["GET"])
@setup_only
def finalize():
    completed = session.get("setup_completed_steps", [])
    if len(completed) < TOTAL_STEPS:
        flash("Please complete all setup steps before finalizing.", "warning")
        return redirect(url_for("setup.index"))

    fqdn      = os.environ.get("SERVER_FQDN", "localhost")
    cert_path = os.environ.get("CERT_PATH",   "")
    key_path  = os.environ.get("KEY_PATH",    "")
    app_port  = int(os.environ.get("APP_PORT", 8080))

    ok, msg = write_https_nginx_config(fqdn, cert_path, key_path, app_port)
    if ok:
        svc.mark_setup_complete()
        flash("🎉 Setup complete! Orbit is now running over HTTPS.", "success")
    else:
        flash(f"Setup saved but Nginx HTTPS switch failed: {msg} — "
              "You may need to configure HTTPS manually.", "warning")

    session.pop("setup_completed_steps", None)
    session.pop("webex_info", None)
    return redirect(url_for("setup.complete"))


