# ── Helpers ───────────────────────────────────────────────────────────────────

def _mark_step_complete(step: int) -> None:
    completed = session.get("setup_completed_steps", [])
    if step not in completed:
        completed.append(step)
    session["setup_completed_steps"] = completed
    session.modified = True


def _populate_timezone_choices(form) -> None:
    import pytz
    zones = [(tz, tz) for tz in pytz.common_timezones]
    form.timezone.choices = zones


def _parse_saml_metadata(form) -> None:
    """Parse uploaded SAML metadata XML and auto-fill form fields."""
    try:
        import xml.etree.ElementTree as ET
        xml_data = form.saml_metadata_file.data.read()
        root = ET.fromstring(xml_data)
        ns = {"md": "urn:oasis:names:tc:SAML:2.0:metadata",
              "ds": "http://www.w3.org/2000/09/xmldsig#"}

        entity_id = root.get("entityID", "")
        sso_el = root.find(".//md:SingleSignOnService", ns)
        slo_el = root.find(".//md:SingleLogoutService", ns)
        cert_el = root.find(".//ds:X509Certificate", ns)

        if entity_id:
            form.saml_entity_id.data = entity_id
        if sso_el is not None:
            form.saml_sso_url.data = sso_el.get("Location", "")
        if slo_el is not None:
            form.saml_slo_url.data = slo_el.get("Location", "")
        if cert_el is not None:
            form.saml_certificate.data = cert_el.text.strip()
    except Exception:
        pass   # Silent — user can fill fields manually
